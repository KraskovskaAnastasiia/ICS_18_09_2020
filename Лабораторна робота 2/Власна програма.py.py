a = int (input("Введіть цифру від 1 до 5, щоб отримати прислів'я"))

if (a == 1):
    print("Майська роса коням краще вівса.")
elif (a == 2):
    print("Посієш вчасно, то і вродить рясно.")
elif (a == 3):
    print("Раді люди літу, а бджоли цвіту.")
elif (a == 4):
    print("Садок літом — як кожух зимою.")
elif (a == 5):
    print("Хто рано посіє, рано й пожне.")
else:
    print("Ви ввели неправильну цифру")



